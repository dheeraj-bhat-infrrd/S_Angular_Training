package com.realtech.socialsurvey.core.entities;

/**
 * LinkedIn end date
 *
 */
public class EndDate {

	private int month;
	private int year;

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
}
